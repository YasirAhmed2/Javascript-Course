// arithmetic operators
let a=10;
let b=3;
console.log(a+b);
console.log(a-b);
console.log(a*b);
console.log(a/b);
console.log(a%b);

// comparison operators
console.log(5=="5");
console.log(x===y);  // strict equality
console.log(x>y);
console.log(x<y);
console.log(x>=y);
console.log(x<=y);
console.log(x!=y);
console.log(x!==y); // strict not equal

// logical operators
console.log(true && false);
console.log(true || false);
console.log(!true);

// assignment operators
let x=10;
x+=5;
console.log(x);